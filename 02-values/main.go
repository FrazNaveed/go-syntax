/**
Go has various value types including strings, integers, floats, booleans
*/

package main

import "fmt"

func main() {
	//strings can be added with +
	fmt.Println("Go" + "lang")

	// integers can be added
	fmt.Println(1 + 1)

	// floats
	fmt.Println(3.1 / 2.1)

	// booleans
	fmt.Println(true && false)

}
